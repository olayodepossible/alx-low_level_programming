#include <stdio.h>

/**
 * main - prints the size of various C programming data types
 * Return: 0 provided it runs with no error
*/

int main(void)
{
	printf("Size of a char data-type: %d bytes(s)\n", sizeof(char));
	 printf("Size of an int data-type: %d bytes(s)\n", sizeof(int));
	 printf("Size of a long int data-type: %d bytes(s)\n", sizeof(long int));
	 printf("Size of a long long data-type: %d bytes(s)\n", sizeof(long long));
	 printf("Size of a float data-type: %d bytes(s)\n", sizeof(float));
	return (0);

}
